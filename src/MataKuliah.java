class MataKuliah {
    String nama;
    int sks;
    String jenis;
    String kode;
    String semester;

    public MataKuliah(String nama, int sks, String jenis, String kode, String semester) {
        this.nama = nama;
        this.sks = sks;
        this.jenis = jenis;
        this.kode = kode;
        this.semester = semester;
    }
}