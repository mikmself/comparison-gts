class Dosen {
    String nama;
    String nip;
    String jabatan;
    String fakultas;
    String email;

    public Dosen(String nama, String nip, String jabatan, String fakultas, String email) {
        this.nama = nama;
        this.nip = nip;
        this.jabatan = jabatan;
        this.fakultas = fakultas;
        this.email = email;
    }
}