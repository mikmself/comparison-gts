class Ruang {
    String nama;
    String jenis;
    int kapasitas;
    boolean status;
    boolean tersedia;

    public Ruang(String nama, String jenis, int kapasitas, boolean status, boolean tersedia) {
        this.nama = nama;
        this.jenis = jenis;
        this.kapasitas = kapasitas;
        this.status = status;
        this.tersedia = tersedia;
    }
}